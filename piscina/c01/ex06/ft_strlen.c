#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	ft_strlen(char *str)
{
	int	v;

	v = 0;
	while (*str != '\0')
	{
		str++;
		v++;
	}
	return (v);
}
